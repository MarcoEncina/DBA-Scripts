EXEC @ReturnCode = msdb.dbo.sp_add_jobstep @job_id=@jobId, @step_name=N'CYCLE SQLSERVER ERRORLOG', 
		@step_id=1, 
		@cmdexec_success_code=0, 
		@on_success_action=3, 
